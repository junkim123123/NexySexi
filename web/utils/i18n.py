def get_current_language() -> str:
    """Get current language from session state."""
    if "language" not in st.session_state:
        st.session_state.language = DEFAULT_LANGUAGE
    return st.session_state.language


def set_language(lang_code: str) -> None:
    """Set current language."""
    if lang_code in SUPPORTED_LANGUAGES:
        st.session_state.language = lang_code


def t(key: str) -> str:
    """Translate a key to current language."""
    lang = get_current_language()
    
    if key not in TRANSLATIONS:
        return key
    
    translations = TRANSLATIONS[key]
    
    # Return translation for current language, fallback to English
    return translations.get(lang, translations.get("en", key))


def render_language_selector():
    """Render language selector in sidebar or header."""
    current = get_current_language()
    
    # Create options
    options = list(SUPPORTED_LANGUAGES.keys())
    labels = [f"{SUPPORTED_LANGUAGES[code]['flag']} {SUPPORTED_LANGUAGES[code]['name']}" for code in options]
    
    # Find current index
    current_idx = options.index(current) if current in options else 0
    
    # Render selector
    selected_label = st.selectbox(
        "ЁЯМР Language",
        options=labels,
        index=current_idx,
        key="language_selector",
        label_visibility="collapsed"
    )
    
    # Get selected code
    selected_idx = labels.index(selected_label)
    selected_code = options[selected_idx]
    
    # Update if changed
    if selected_code != current:
        set_language(selected_code)
        st.rerun()


def render_language_selector_minimal():
    """Render minimal language selector (just flags)."""
    current = get_current_language()
    current_info = SUPPORTED_LANGUAGES[current]
    
    with st.popover(f"{current_info['flag']} {current_info['name']}"):
        st.markdown("**ЁЯМР Select Language**")
        
        for code, info in SUPPORTED_LANGUAGES.items():
            if st.button(
                f"{info['flag']} {info['name']}",
                key=f"lang_{code}",
                use_container_width=True,
                type="primary" if code == current else "secondary"
            ):
                set_language(code)
                st.rerun()
